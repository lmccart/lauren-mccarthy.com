<p class="summary"><span class="red">Happiness Hat.</span> A wearable conditioning device that detects if you're smiling and provides pain feedback if you're not. Frowning creates intense pain but a full smile leaves you pain free! The first in a series of Tools for Improved Social Inter-Acting.</p>
<div id="description">
<p class="description">An enclosed bend sensor attaches to the cheek and measures smile size, a servo motor moves a metal spike into the head inversely proportional to the degree of smile. Through repeated use of this conditioning device you can train your brain to smile all the time. The device runs on Arduino.<br/><br/>
A smile is a simple action that has the power to make you and everyone around you feel better. Just using the muscles to smile can make you feel happier. Seeing someone else smiling triggers mirror neurons in your own brain, causing you to unconsciously smile yourself. What is the potential of feedback to improve the way we act and feel? On the other hand, how often does the appearance we project misrepresent what we are really thinking and feeling? How do we reconcile these ideas? 
