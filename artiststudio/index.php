<p class="title">ARTIST STUDIO <span class="date">2007</span></p>
<p class="summary">The overlapping, reconfigurable glass walls responded to the site, while allowing the artist to determine the boundaries between public and private space.</p>
<div id="description">
<p class="description">Creating a place for an artist to live, work, and interact with the public as a part of the redevelopment of Boston's Piers was an exciting project. Drawing inspiration from the reflective waves of the surrounding water, I made all the walls glass. An important consideration for the design was the distinction between private and public space. Parts of the walls were translucent and other parts were transparent, and the interior walls slid on tracks. This reconfigurable quality allowed each individual artist to choose what was visible to the public and what was private.</p>




