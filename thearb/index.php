  <p class="description">Completely redesigning the music store experience as a concept project while interning at Continuum was one of my favorite projects and a huge learning experience.  With two principals acting as the client, I conducted research, identified customer archetypes, developed a brand persona and name, mapped a customer journey, and designed each of the touchpoints in the experience.  I finished with a presentation of a 50 page proposal book detailing each of the elements of my design, including shopping system, brand, architecture, furniture, and scent.</p>
  
