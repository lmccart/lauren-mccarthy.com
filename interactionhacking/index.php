<p class='summary'>description</p>

<p class='description'>The workshop typically lasts for 3 hours with 6-15 participants. Participants are given a set of prepartory readings and experimental interaction platforms and tools created by the artists. During the workshop, we prototype various uses for these platforms and tools, attempting to break expectations and patterns of social interaction. The experiments are supplemented by discussion of readings and presentation of other interaction research.<br><br>

Themes covered will include glitch, automation and APIs, computer vision, linguistic analysis, security, surveillance, privacy. We will draw on research from sociology, social psychology, computer science, performance studies, and pop culture.<br><br>

A collaboration between <a class="red" href="http://kylemcdonald.net" target=_blank>Kyle McDonald</a> and <a class="red" href="http://lauren-mccarthy.com">Lauren McCarthy</a>.</p>



