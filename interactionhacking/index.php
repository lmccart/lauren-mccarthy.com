<p class="summary"><span class="red">interaction hacking.</span> How can technology be used to augment, subvert, alter, mediate, and ultimately deepen interaction in a lasting way? Who owns the data created between two people, what if it is captured and used? What if algorithms could understand our relationships and make better interpersonal decisions than we can ourselves? In this workshop, we explore these questions, going beyond knee-jerk reactions, instead choosing to grapple with the gray areas as we live the algorithms first-hand.</p>

