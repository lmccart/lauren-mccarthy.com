<p class="subtitle">Statement</p>
<p>
I am an artist whose work examines the effects of technology on social relationships and identity. My background is in both art and technology; I hold an MFA from UCLA Design | Media Arts, and a BS in Computer Science and a BS in Art and Design from MIT.<br><br>

Specifically, I investigate the effects of big data, algorithms, and surveillance on our social relationships. Can and should a relationship be tracked and analyzed, and who owns the data created between two people? As we’re constantly monitoring each other and finding more ways to consume information, new possibilities for real-time feedback emerge. What are the effects and implications of this, and how could it provoke new ways of relating to each other? In our increasingly networked culture, how do we navigate between collective and individual experience, and how does the control shift? Through technology-based artistic interventions, I create disruptions meant to reveal, question, and subvert underlying expectations, assumptions, and systems of control.<br><br>

I was most recently a resident at Carnegie Mellon STUDIO for Creative Inquiry and Eyebeam where I worked on several apps that used biometric data, linguistic analysis, and computer vision to analyze and improve relationships. My recent work Social Turkers, a performance where I asked Mechanical Turk workers to direct me on dates, was awarded a Rhizome Commission, a NYSCA grant, a Core77 Design Award, an Intel Research Grant, and the Share Prize. I’ve also received a Japan Media Arts award for my Tools for Improved Social Interacting series. My artwork has been shown at Ars Electronica, Share Festival, LACMA, the Conflux Festival, SIGGRAPH, Japan Media Arts Festival, File Festival, and the WIRED Store.<br><br>

My technical and design background is rounded out by extensive experience working in the commercial realm. At Sosolimited and Small Design Firm, I created large-scale immersive environments and web and mobile applications for the London Eye, IBM, US Holocaust Memorial Museum, and the Metropolitan Museum of Art. During my time at Sosolimited, I led the team in the conceptual and technical transition to working on the web. I have also worked at Oblong Industries and the MIT Media Lab, focusing on interactive experiences and interfaces. My most recent commercial project was an interactive conversation monitoring installation for the Tech Museum of Innovation.<br><br>

Another way I explore networked interactions is through my involvement with open source software development. I am involved with openFrameworks, and p5.js, a new version of Processing for the web. Over the past several years, I have initiated and led the design and development of the p5.js project as well as surrounding curriculum, in collaboration with the Processing Foundation. This work has been supported by a Processing Foundation fellowship, a residency at NYU ITP, and grant from RISD and CMU. Since the public launch this fall, it has been incorporated into curricula all around the world, with several books coming out later this year.<br><br>

I am full-time faculty at NYU ITP, where I teach classes that cover topics like social hacking, tactical media, and computational conversation. In the social hacking class, we look critically at the structures of power and control inherent in technologies, and explore possibilities to question, subvert, or alter the social and political ideologies they impose. Over the past year, I’ve also led ITP in updating their curriculum to move to the web, using p5.js among other toolkits. Prior to becoming full-time faculty, I was an adjunct professor and resident researcher at ITP, and adjunct faculty at RISD.<br><br>

My audience spans the art world, everyday users of technology, and those that imagine, design, and develop it. Toward this end, I give talks and work closely with tech focused companies and institutions, like Intel, Institute for the Future, and the Berkman Center. I believe there is a need for artists to participate in these conversations and provoke the hard questions about the future we are developing together.
</p>

