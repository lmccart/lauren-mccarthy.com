<p class="summary">800 photographs transformed this space into a dynamic container for the sights, thoughts, and experiences collected during a daily running ritual.</p>
<div id="description">
<p class="description">The assignment to create a container of some kind on an architectural scale was very open-ended.  I struggled for a while to come up with a structure that would serve as a container on several levels.<br/><br/>
Using my love of running as an inspiration, I sought to create a place that would contain my running experience for public display.
Carrying a camera while I ran throughout Cambridge and Boston, I took over 800 photographs which I strung on fishing line.  Wrapping them around 4 trees, I created a small house.  The structure contained a visual representation of the many places I visit on my runs.  It also served as a container for the obsessive love I feel for the sport; the repititive photographs have both a beautiful and overwhelming quality to them.</p>




