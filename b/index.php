<p class="title">B <span class="date">2010</span></p>
<p class="summary">B attempts to collect and visualize what we perceive as beauty. The goal of the project is to raise awareness of our environment and the ways we perceive it, and to try to understand how we experience beauty.</p>

<div id="description">

<p class="description">B is a mobile application that collects data and a website that visualizes this data. At random moments, users of the application are asked to rate and document their current experience. In this way, B invites the user to consider potential beauty, otherwise lost among the actions of everyday life. The B website makes the collection of experiences accessible to users and the
general public.<br><br>B is not an objective data harvesting framework. B does not intend to provide a meaningful insight on the constituents of beauty. Instead, B focuses on creating awareness and the sharing of experiences.<br><br>Created in collaboration with <a href="http://perceptor.nl" class="red" target=_blank>Casper Schipper</a>.
</p>

