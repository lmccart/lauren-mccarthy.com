  <p class="summary"><span class="red">Showertweet.</span> In August, I was the Twitter Artist in Residence for the Brooklyn Museum of Art. I waterproofed my phone and tweeted everyday from my shower.</p>
  <div id="description">
  <p class="description">For me, the shower is a place for private, intimate reflection and ritual - a time all for myself to focus on my body and my thoughts without interruption. It is a preparation for the day, physically and psychologically. It is a set of routine motions that I can do with little attention to them, leaving my mind free to wander.<br/><br/>
    
    I will create a waterproof casing for my phone [more photos of this process may be found in the 1stfans flickr group], and send one tweet via text message per day from inside the shower. The subject of the tweets will be whatever thoughts come to mind as I wash myself. They will mix observations of the physical sensations of cleaning my body with thoughts that arise through the process.<br/><br/>
    
    The practice will explore the way twitter/technology is incorporated into daily ritual, and provide a moment for me to focus on the significance and experience of cleaning myself, while engaging simultaneously with my physical body and my mental processes. Others will be able to share in this, as my normally private shower reflection is made public through the twitter broadcast.<br/><br/>
    
    Each person has their own personal shower ritual and it is rarely given much thought or discussed. Through this project I would hope to inspire others to take a minute while showering to consciously consider their routines, their bodies, and their ideas.<br/><br/>
    
    I also want to explore the effect technology has on the boundary between private and public life. What happens if the most intimate experiences are shared online? How is this sharing different than if it were to occur in physical space? What place do our physical bodies have in our increasingly technologically mediated world?</p>
