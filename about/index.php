			<p>Lauren McCarthy is an artist and programmer based in Brooklyn, NY. She is full-time faculty at NYU ITP, and recently a resident at <a href="http://studioforcreativeinquiry.org">CMU STUDIO for Creative Inquiry</a> and <a href="http://eyebeam.org" target=_blank>Eyebeam</a>. She holds an MFA from UCLA and a BS Computer Science and BS Art and Design from MIT. Her work explores the structures and systems of social interactions, identity, and self-representation, and the potential for technology to mediate, manipulate, and evolve these interactions.<br><br>
			
			Lauren has worked on installations for the London Eye, North Carolina Museum of Natural Sciences, IBM, US Holocaust Memorial Museum, Metropolitan Museum of Art, and Thomas Jeffersonâ€™s home at Monticello.<br><br>
			
			
