<p class="title">SELFSOURCE <span class="date">a proposal</span></p>
<p class="summary">A mobile device application for capturing and sharing self identities, creating a browseable archive of self identities to be used by those needing a break from their own.</p>

<div id="holder">
<p class="summary">Description</p>
<p class="description"><strong>Overview</strong><br/>Selfsource is a mobile device application for capturing and sharing self identities, creating a browsable archive of identities to be used by those needing a break from their own. It will be available for iPhone and Android. Selfsource has two modes - capture and share.</p>

<p class="description"><strong>Self Identity Capture Mode</strong><br/>

<img class="full" src="./selfsource-01.png"/>
Users can create a model of their self identity to share with others in self identity capture mode. In this mode, users first fill out some basic info about themselves, then they are prompted periodically throughout the day by their mobile device to answer short questions about their current self expression.<br/><br/>
For example:<br/>
what are you doing?<br/>
what was the last thing you said?<br/>
where are you?<br/>
what are you thinking?<br/>
what are you wearing?<br/>
how are you feeling?
<br/><br/>
This information is aggregated and analyzed to create a model of the user’s self identity which is then added to a browsable database of identities to be used by others.</p>

<p class="description"><strong>Self Identity Share Mode</strong><br/>

<img class="full" src="./selfsource-02.png"/>
In self identity share mode, a user can browse the database of identities, searching for specific features and characteristics, and choose one to use for a day, week, lifetime... Once the user selects one, his mobile device directs him in the adoption of the new identity by creating a schedule at the beginning of each day and prompting him periodically throughout with things to say, do, and think about. The key idea here is that rather than changing the tangible aspects of one's life (job, name, location, etc), the prompts create a subtle shift in the qualitative aspects of day to day activities (think adverb, rather than verb), allowing the user to adopt a new identity without having to change everything about his life. In this way, the new identity fits like a layer into the framework of the user's normal life, replacing the qualitaties and characteristics that define his personality and state of mind.
<br/><br/>
Users can leave feedback and ratings of self identities they try out, and popular ones will rise to the top of archive searches. The app will also log and display stats of identity use, so users can see how many people use each and for how long.</p>

</div>

<div id="holder">
<p class="summary">Intent</p>
<p class="description">The intent is to invite users to reflect on and question the basis for their concept of their own self identities. Can it be captured with a data set? If our actions, appearance, thoughts change are we still the same person? How often do we limit ourselves to a tiny subset of possibilities because the rest seems outside the arbitrary realm of ‘things we would typically do’? What freedom does the adoption of someone else’s identity give us to play and explore beyond our normal selves?</p>
</div>



