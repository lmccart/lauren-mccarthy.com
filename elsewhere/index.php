<p class="description"><strong>Revolving face space.</strong> Two people can sit and talk, with heads inside rotating face tube. Each tube has a number of different faces on all sides, created by masks found around elsewhere. Whenever necessary, each person can easily rotate tube to inhabit a new face more suited to their current mood, topic of conversation, desired persona, etc. When not in use, revolving face space swings up into side wall or raises up into ceiling. (Location: 1st floor window seat)</p>

<img class="full" src="./proj1.jpg"/>
<p class="description"><strong>Rub a dub tub.</strong> Two people can sit and talk, while simultaneously moving knobs to control body parts on the opposite side of the tub that touch the other person. Maybe this is a shower curtain like thing between them? (Location: 3rd floor bathtub)</p>

<img class="fullTall" src="./proj2.jpg"/>
<p class="description"><strong>Take a moment.</strong> A small room for two people to sit and be together, at random times, a curtain between them automatically shuts with a message that reads "take a moment". After a random amount of time, the curtain reopens and conversation can continue. (Location: TBD? 3rd floor room?)</p>



