<p class="summary"><span class="red">Relation Station.</span> A site-specific, found material installation that allowed people to explore alternative interactions. People could choose from a wall of three different devices to augment their interactions with each other. Created while in residency at <a class="gray" target=_blank href="http://elsewhereelsewhere.org">Elsewhere Collaborative</a>.</p>

<div id="description">

<p class="description"><strong>About face</strong><br><br>
<img class="skinny" src="./elsewhere5w.jpg"/>
Two hats were made that featured eight different faces. The wearers could rotate in the faces as necessary.</p>

<br><br><br>

<p class="description"><strong>Touch us</strong><br><br>
<img class="skinny" src="./elsewhere6w.jpg"/>
A frame that hooked into the shelves of the reading carroll held rubber doll body parts that people could control and use to touch each other during conversation.
</p>

<br><br>

<p class="description"><strong>Take a moment</strong><br><br>
<img class="skinny" src="./elsewhere7w.jpg"/>
An arduino controlled fan hooked onto the back edge of the reading carroll, raising and lowering at random intervals to allow people to take a break from their conversation and experience moments to themselves to reflect.
</p>


