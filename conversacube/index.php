<p class="summary"><span class="red">Conversacube.</span> Don't know the right thing to say? Stumbling over words? Do away with uncomfortable conversation. Conversacube is a conversation aid device that will prompt you what to say and do and guide everyone toward smooth, comfortable conversation every time.</p>
<div id="description">
<p class="description">
<strong>Description</strong><br/><br/>	
The Conversacube is a small box meant to form the centerpiece of any conversation situation. The box sits in the middle of all conversants, with one face facing each person. Each outward face of the box has a small screen and a microphone embedded just inside. As the conversation progresses, each person is personally prompted with directions or lines to keep the conversation running seamlessly with minimal awkward or uncomfortable moments. The microphones monitor audio levels of each participant and the cube responds accordingly, adjusting prompts to enliven, mediate conflict, or balance conversation as necessary.<br/><br/><br/>
<strong>Concept</strong><br/><br/>
The intent is to create a tool that on one hand, explores the idea of an actual commercial product that uses technology to improve interactions, and on the other hand, is critical of our dependence on technology and choreographed social routines, hinting at a dystopic future where we sacrifice our autonomy to avoid having to face anything uncomfortable. The Conversacube asks users to consider in which of these directions we’re heading. Or is it both? Are we consciously aware of the future we’re building with all of our technological innovation? Are there ways we can use technology to expand our awareness and move toward more connected social relationships?<br/><br/><br/>
<strong>Performance/Installation</strong><br/><br/>
I travel to various public locations with the Conversacube, including subway stops, street corners, and cafes. I take on the persona of a market researcher conducting user tests with the device as if it is a product coming soon to market.
<br/><br/>User feedback surveys are administered, soliciting opinions on the look and feel of the device, the overall quality of their experience with it, and whether they would buy one for their homes or workplaces. The goal is to create a situation where the public participants were not sure whether this is a serious product or an art piece, hopefully inspiring thoughts about the implications and potential of such a device or related technologies.

