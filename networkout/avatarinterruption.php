<p class="summary">Networkout explored the boundary between physical and virtual living by examining it through the lenses of online social networking and gym culture.</p>
<div id="description">
<p class="description">I used one of the TV monitors in the MIT Zesiger Athletic Center to play Avatar Interruption. The piece was created from a prerecorded episode of a program called Top Chef. Every 5-10 minutes, the program was interrupted by the video image of a Second Life avatar working out in a Second Life gym, mimicking the motions of those in the physical gym. The avatars gave short monologues that sarcastically questioned the motivations and behavior of gym goers. The clip would play for a minute, then the program would resume as normal. The video was 30 minutes long and looped continuously.</p>
