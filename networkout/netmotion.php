<p class="description">Net Motion involved the creation of a wearable object or piece of clothing, that could sense the body position of the wearer. The clothing could communicate wirelessly with others of its type. When two or more people wearing the device were in roughly the same body position, the clothing would begin to glow. The degree of brightness of the glow would be proportional to the number of people that were in the same position.<br/><br/>Participants could try to adjust their position to find a network or group of people that would all glow. Alternatively, they could try to find completely unique positions that would be indicated by a lack of light.</p>

