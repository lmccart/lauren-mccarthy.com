  <p class="summary">Networkout explored the boundary between physical and virtual living by examining it through the lenses of online social networking and gym culture.</p>
  <div id="description">
  <p class="description">As technology continues to advance, the boundary between virtual and physical living becomes less clear. My thesis aimed to explore this boundary by examining it through the lenses of online social networking culture and gym culture.<br/><br/>
  These two trends, one virtual and one physical, have several correlations. Both are user focused and provide the opportunity to create a representation of oneself. Each also has elements of voyeurism and surveillance, isolation and connection, and a sense of the collective.  Through a series of interventions that addressed both cultures, I questioned our interaction with each, and the mediation of the two.</p>
  
