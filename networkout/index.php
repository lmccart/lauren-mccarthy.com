      <p>The art work I produced over the course of the semester was comprised of a series of projects that blended elements of online and gym culture, and investigated the motivations, behaviors, effects, and structures within each. The development of this work occured from two directions - some were based in the gym and brought in ideas of online culture, while others were web-based and incorporate elements of gym culture.<br/><br/>
      The series of projects was formed through an experimental production process based on specific research. This process was comprised of my participation and observations within each culture, research investigating the various discourses relating to each, study of related artists and work, and iterative design, testing and critique of my own work. The series of experiments culminated in two large scale public installations.</p></li>
